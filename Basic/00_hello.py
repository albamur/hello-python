# ---------------------------------- FUNCIÓN TYPE() ---------------------------------- 
# La función print() se utiliza para consultar el tipo de dato

print(type("Soy un dato str")) # Tipo 'str'
print(type(5)) # Tipo 'int'
print(type(1.5)) # Tipo 'float'
print(type(3 + 2j)) # Tipo 'complex'
print(type(True)) # Tipo 'boolean'


